class EmergencyContact {
  String id = " ";
  int contact = 0;
  String contactName = " ";
  String contactPhone = " ";

  EmergencyContact(
      String _id, int _contact, String _contactName, String _contactPhone) {
    this.id = _id;
    this.contact = _contact;
    this.contactName = _contactName;
    this.contactPhone = _contactPhone;
  }
}
